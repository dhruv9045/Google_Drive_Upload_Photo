    /** Build a Google SignIn client. */
    private GoogleSignInClient buildGoogleSignInClient() {
        GoogleSignInOptions signInOptions =
                new GoogleSignInOptions.Builder(GoogleSignInOptions.DEFAULT_SIGN_IN)
                        .requestScopes(Drive.SCOPE_FILE)
                        .requestEmail()
                        .build();
        mGoogleApiClient = new GoogleApiClient.Builder(this)
                .addApi(Auth.GOOGLE_SIGN_IN_API, signInOptions)
                .build();
        mGoogleApiClient.connect();
        super.onStart();         //Optional
        return GoogleSignIn.getClient(this, signInOptions);
    }

    /*  Create a new file and save it to Drive.  */
    private void saveFileToDrive() {
        // Start by creating a new contents, and setting a callback.
        Log.i(TAG, "Creating new contents.");
        final Bitmap image = mBitmapToSave;

        mDriveResourceClient
                .createContents()
                .continueWithTask(
                        new Continuation<DriveContents, Task<Void>>() {
                            @Override
                            public Task<Void> then(@NonNull Task<DriveContents> task) throws Exception {
                                return createFileIntentSender(task.getResult(), image);
                            }
                        })
                .addOnFailureListener(
                        new OnFailureListener() {
                            @Override
                            public void onFailure(@NonNull Exception e) {
                                Log.w(TAG, "Failed to create new contents.", e);
                            }
                        });
    }

    /**
     * Creates an {@link IntentSender} to start a dialog activity with configured {@link
     * CreateFileActivityOptions} for user to create a new photo in Drive.
     */
    private Task<Void> createFileIntentSender(DriveContents driveContents, Bitmap image) {
        Log.i(TAG, "New contents created.");

        // Get an output stream for the contents.
        OutputStream outputStream = driveContents.getOutputStream();
        // Write the bitmap data from it.
        ByteArrayOutputStream bitmapStream = new ByteArrayOutputStream();
        image.compress(Bitmap.CompressFormat.JPEG, 100, bitmapStream);
        int options = 100;
        while (bitmapStream.toByteArray().length / 1024 > 1020) {  //Cycle to determine if the compressed image is greater than 1000kb, greater than continue compression
            bitmapStream.reset();//Reset the BIOS to clear it
            //First parameter: picture format, second parameter: picture quality, 100 is the highest, 0 is the worst, third parameter: save the compressed data stream
            image.compress(Bitmap.CompressFormat.JPEG, options, bitmapStream);//Here, the compression options are used to store the compressed data in the BIOS
            options -= 90;//90 less each time
        }
        byte[] imageImageByte = bitmapStream.toByteArray();
        long lengthbmp =imageImageByte.length;
        Log.i(TAG, "Image size of Camera"+lengthbmp);
        try {
            outputStream.write(bitmapStream.toByteArray());
        } catch (IOException e) {
            Log.w(TAG, "Unable to write file contents.", e);
        }

        // Create the initial metadata - MIME type and title.
        // Note that the user will be able to change the title later.
        MetadataChangeSet metadataChangeSet =
                new MetadataChangeSet.Builder()
                        .setMimeType("image/jpeg")
                        .setTitle("Android Photo.png")
                        .build();
        // Set up options to configure and display the create file activity.
        CreateFileActivityOptions createFileActivityOptions =
                new CreateFileActivityOptions.Builder()
                        .setInitialMetadata(metadataChangeSet)
                        .setInitialDriveContents(driveContents)
                        .build();

        return mDriveClient
                .newCreateFileActivityIntentSender(createFileActivityOptions)
                .continueWith(
                        new Continuation<IntentSender, Void>() {
                            @Override
                            public Void then(@NonNull Task<IntentSender> task) throws Exception {
                                startIntentSenderForResult(task.getResult(), REQUEST_CODE_CREATOR, null, 0, 0, 0);
                                return null;
                            }
                        });
    }


    @Override
    protected void onActivityResult(final int requestCode, final int resultCode, final Intent data) {
        super.onActivityResult(requestCode, resultCode, data);
        switch (requestCode) {
            case REQUEST_CODE_SIGN_IN:
                Log.i(TAG, "Sign in request code");
                // Called after user is signed in.
                if (resultCode == RESULT_OK) {
                    Log.i(TAG, "Signed in successfully.");
                    // Use the last signed in account here since it already have a Drive scope.
                    mDriveClient = Drive.getDriveClient(this, GoogleSignIn.getLastSignedInAccount(this));
                    // Build a drive resource client.
                    mDriveResourceClient =
                            Drive.getDriveResourceClient(this, GoogleSignIn.getLastSignedInAccount(this));
                    Toast.makeText(getApplicationContext(),"Signed in Successfully",Toast.LENGTH_LONG).show();
                }
                break;
            case REQUEST_CODE_CAPTURE_IMAGE:
                Log.i(TAG, "capture image request code");
                // Called after a photo has been taken.
                if (resultCode == Activity.RESULT_OK) {
                    Log.i(TAG, "Image captured successfully");

                    startActivityForResult(Intent.createChooser(new Intent().setAction(Intent.ACTION_GET_CONTENT)
                            .setType("image/*"), "Select Picture"), PICK_SINGLE_IMAGE);
                }
                break;
            case REQUEST_CODE_CREATOR:
                Log.i(TAG, "creator request code");
                // Called after a file is saved to Drive.
                if (resultCode == RESULT_OK) {
                    Log.i(TAG, "Image successfully saved.");
                }
                break;
            case PICK_SINGLE_IMAGE:
                Log.i(TAG, "creator request code");
                // Get the Image from data
                if(resultCode == RESULT_OK) {
                    Uri imageUri =data.getData();

                    InputStream imageStream = null;
                    try {
                        imageStream = getContentResolver().openInputStream(imageUri);
                    } catch (FileNotFoundException e) {
                        e.printStackTrace();
                    }

                    Bitmap image = BitmapFactory.decodeStream(imageStream);
                    mBitmapToSave=null;
                    mBitmapToSave =(Bitmap) image ;
                    saveFileToDrive();


                }
                break;
            case PICK_IMAGE_MULTIPLE:
                Log.i(TAG, "creator request code");
                // Get the Image from data
                if(resultCode == RESULT_OK) {
                    String[] filePathColumn = {MediaStore.Images.Media.DATA};
                    imagesEncodedList = new ArrayList<String>();

                    if (data.getClipData() != null) {
                        ClipData mClipData = data.getClipData();
                        ArrayList<Uri> mArrayUri = new ArrayList<Uri>();
                        for (int i = 0; i < mClipData.getItemCount(); i++) {
                            ClipData.Item item = mClipData.getItemAt(i);
                            Uri uri = item.getUri();
                            InputStream stream = null;
                            try {
                                stream = getContentResolver().openInputStream(uri);

                            } catch (FileNotFoundException e) {
                                e.printStackTrace();
                            }

                            Bitmap img = BitmapFactory.decodeStream(stream);
                            mBitmapToSave = null;
                            mBitmapToSave = (Bitmap) img;
                            mArrayUri.add(uri);
                            try {
                                // Get the cursor
                                cursor = getContentResolver().query(uri, filePathColumn, null, null, null);
                                // Move to first row
                                if (cursor != null && cursor.moveToFirst()) {
                                    int columnIndex = cursor.getColumnIndex(filePathColumn[0]);
                                    imageEncoded = cursor.getString(columnIndex);
                                    imagesEncodedList.add(imageEncoded);
                                }
                            } finally {
                                if (cursor != null)
                                    cursor.close();

                            }
                            saveFileToDrive();


                            Log.v("LOG_TAG", "Selected Images" + mArrayUri.size());

                        }
                    }
                }

        }
    }
